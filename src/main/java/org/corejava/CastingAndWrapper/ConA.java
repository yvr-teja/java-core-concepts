package org.corejava.CastingAndWrapper;

public class ConA {
    public String NameA="";
    public String AgeA="";

    public ConA()//Non - Argu Constuctor
    {
        this.NameA="Ravi Teja";
        this.AgeA="30";
    }
    public ConA(String name, String age) //Argu Constuctor
    {
        this.NameA=name;
        this.AgeA=age;
    }
}
